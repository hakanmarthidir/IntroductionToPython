# ARITHMETIC OPERATORS

number1 = int(input("Give me Number 1 : "))
number2 = int(input("Give me Number 2 : "))

# +
total = number1 + number2
print(total)

# -
sub = number1 - number2
print(sub)

# *
mult = number1 * number2
print(mult)

# /
division = number1 / number2
print(division)

# //
floDiv = number1 // number2
print(floDiv)

# %
mod = number1 % number2
print(mod)

#  **
pow = number1 ** number2
print(pow)